package com.forLoop;

//odd number means = 2*n+1  , n is number like = 2*0+1=1, 2*1+1=3,2*2+1=5....
//even number = 2n, n is number like = 2*1 = 2, 2*2=4, 2*3=6....

public class PrintOddNumber {
	public static void main(String[] args) {
		for(int i=1; i<10; i++)
		{
			System.out.println(2*i+1);
		}
	}

}
